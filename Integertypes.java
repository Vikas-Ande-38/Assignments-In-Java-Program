public class Integertypes {
    public static void main(String[] args) {
        byte a = 100;
        short b = 30000;
        int c = 100000;
        long d = 123456789L;

        System.out.println("Byte: " + a);
        System.out.println("Short: " + b);
        System.out.println("Int: " + c);
        System.out.println("Long: " + d);
    }
}
